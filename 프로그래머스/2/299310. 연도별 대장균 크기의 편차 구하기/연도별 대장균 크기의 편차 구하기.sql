SELECT YEAR(DIFFERENTIATION_DATE) AS YEAR, 
  MAX_SIZE - A.SIZE_OF_COLONY AS YEAR_DEV, A.ID
FROM ECOLI_DATA A
JOIN(SELECT YEAR(DIFFERENTIATION_DATE) AS YEAR,
     MAX(SIZE_OF_COLONY) AS MAX_SIZE
     FROM ECOLI_DATA
     GROUP BY YEAR(DIFFERENTIATION_DATE)) B
     ON B.YEAR = YEAR(A.DIFFERENTIATION_DATE)
ORDER BY YEAR, YEAR_DEV;
